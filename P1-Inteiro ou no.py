inteiro = int(input("Por favor, insira o número inteiro.\n"))

if inteiro>0:
    print("O número digitado é um número positivo.")
elif inteiro<0:
    print("O número digitado é um número negativo.")
else:
    print("O número digitado é zero, ou seja, um número neutro.")